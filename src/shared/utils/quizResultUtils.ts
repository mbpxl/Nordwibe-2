export const getResultMessage = (percent: number): string => {
  if (percent === 100) {
    return "У вас отличный результат! Вы прекрасно ориентируетесь в теме и уверенно отвечаете на все вопросы. Это значит, что вы умеете находить ключевые моменты, грамотно анализировать информацию и применять знания на практике. Такой уровень подготовки поможет вам принимать правильные решения и избегать ошибок. Продолжайте в том же духе — у вас всё получится!";
  } else if (percent >= 60) {
    return "Хороший результат! Вы уверенно справились с большей частью вопросов и продемонстрировали хорошие знания. Да, есть несколько моментов, которые стоит повторить, но в целом вы уже понимаете суть и знаете, как действовать в большинстве ситуаций. Немного практики и внимания к деталям — и вы сможете выйти на уровень эксперта.";
  } else {
    return "Нужно повторить материал! Вам удалось правильно ответить лишь на часть вопросов. Это значит, что материал ещё не усвоен полностью, и есть риск упустить важные нюансы. Не расстраивайтесь: тест показал, над чем стоит поработать. Вернитесь к урокам, повторите ключевые моменты — и следующий результат будет гораздо лучше!";
  }
};

export const getResultColor = (percent: number): string => {
  if (percent === 100) return "bg-green-500";
  if (percent >= 60) return "bg-yellow-500";
  return "bg-red-500";
};
